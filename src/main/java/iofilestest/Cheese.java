package iofilestest;

public class Cheese {
    private String name;
    private double laktoz;

    public Cheese(String name, double laktoz) {
        this.name = name;
        this.laktoz = laktoz;
    }

    public String getName() {
        return name;
    }

    public double getLaktoz() {
        return laktoz;
    }
}
